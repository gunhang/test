public class AuthCodeVO {
	private String authGrpCode; //권한그룹코드
	private String authGrpName; //권한그룹이름
	private String authGrpDesc; //권한그룹설명
	
	
	public String getAuthGrpCode() {
		return authGrpCode;
	}
	public void setAuthGrpCode(String authGrpCode) {
		this.authGrpCode = authGrpCode;
	}
	public String getAuthGrpName() {
		return authGrpName;
	}
	public void setAuthGrpName(String authGrpName) {
		this.authGrpName = authGrpName;
	}
	public String getAuthGrpDesc() {
		return authGrpDesc;
	}
	public void setAuthGrpDesc(String authGrpDesc) {
		this.authGrpDesc = authGrpDesc;
	}
	
	
}                      
